    private String SelectMonth() {
        String m = null;

        if (month.getMonth() == 0) {
            m = "January";
        }
        if (month.getMonth() == 1) {
            m = "February";
        }
        if (month.getMonth() == 2) {
            m = "March";
        }
        if (month.getMonth() == 3) {
            m = "April";
        }
        if (month.getMonth() == 4) {
            m = "May";
        }
        if (month.getMonth() == 5) {
            m = "June";
        }
        if (month.getMonth() == 6) {
            m = "July";
        }
        if (month.getMonth() == 7) {
            m = "August";
        }
        if (month.getMonth() == 8) {
            m = "September";
        }
        if (month.getMonth() == 9) {
            m = "October";
        }
        if (month.getMonth() == 10) {
            m = "November";
        }
        if (month.getMonth() == 11) {
            m = "December";
        }

        return m;

    }

