    private void trainer_logbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trainer_logbuttonActionPerformed
        String tuser = tuserName.getText();
        String tpa = new String(tpass.getPassword());

        if (tuser.equals("") || tpa.equals("")) {
            JOptionPane.showMessageDialog(null, " Empty Password or UserName", "Warning !!!", JOptionPane.ERROR_MESSAGE);
        }
        try {
            Class.forName("com.mysql.jdbc.Driver");
            database = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/kazi_gym", "root", "");
            String sql = "select * from trainer_info where username = ? and password = ?";
            PreparedStatement p = database.prepareStatement(sql);
            p.setString(1, tuser);
            p.setString(2, tpa);
            ResultSet ans = p.executeQuery();

            if (ans.next()) {
                dispose();
                TrainerMain tmp = new TrainerMain();
                tmp.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, " Invalid Password or UserName", "Warning", JOptionPane.ERROR_MESSAGE);
                tuserName.setText("");
                tpass.setText("");
            }
            database.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_trainer_logbuttonActionPerformed

